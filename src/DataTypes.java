

public class DataTypes {
    public static void main(String[] args) {
        String address = "No 14 Ilo \"Awela\" Street\n, Lagos";
        int  speed = 300;
        float  size = 3.4f;
        boolean isActive = true;
        double length = 3.2;
        char alpha = 'c';

        System.out.println("this is a string " + address);
        System.out.println("this is a float " + size);
        System.out.println("this is a integer " + speed);
        System.out.println("this is a boolean " + isActive);
        System.out.println("this is a double " + length);
        System.out.println("this is a char " + alpha);
    }
}
